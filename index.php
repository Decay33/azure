  <!-- JavaScript to Handle Like Buttons - Updated with visual feedback -->
  <script>
    // Function to send a like via AJAX
    function sendLike(gameId) {
      // Visual feedback - show loading state
      var btn = document.querySelector('.like-button[data-game-id="' + gameId + '"]');
      btn.innerHTML = '<i class="fa fa-spinner fa-spin"></i>';
      btn.disabled = true;
      
      var xhr = new XMLHttpRequest();
      xhr.open("POST", "like.php", true);
      xhr.setRequestHeader("Content-Type", "application/x-www-form-urlencoded");
      xhr.onreadystatechange = function() {
        if (xhr.readyState === 4 && xhr.status === 200) {
          var response = JSON.parse(xhr.responseText);
          if (response.success) {
            document.getElementById("like-count-" + gameId).innerText = response.like_count;
            // Mark that this browser has liked this game
            localStorage.setItem("liked-" + gameId, "true");
            // Update button appearance
            btn.innerHTML = '<i class="fa fa-heart"></i> Liked';
            
            // Add a small animation to the like count
            var countElement = document.getElementById("like-count-" + gameId);
            countElement.classList.add('like-count-updated');
            setTimeout(function() {
              countElement.classList.remove('like-count-updated');
            }, 1000);
          } else {
            console.error("Error: " + response.error);
            btn.innerHTML = '<i class="fa fa-heart"></i> Like';
            btn.disabled = false;
          }
        }
      };
      xhr.send("game_id=" + encodeURIComponent(gameId));
    }

    // Function to fetch the initial like count from the server
    function fetchLikeCount(gameId) {
      var xhr = new XMLHttpRequest();
      xhr.open("GET", "get_likes.php?game_id=" + encodeURIComponent(gameId), true);
      xhr.onreadystatechange = function() {
        if (xhr.readyState === 4 && xhr.status === 200) {
          var response = JSON.parse(xhr.responseText);
          if (response.success) {
            document.getElementById("like-count-" + gameId).innerText = response.like_count;
          }
        }
      };
      xhr.send();
    }

    // Attach event listeners once the DOM is loaded
    document.addEventListener("DOMContentLoaded", function() {
      var likeButtons = document.getElementsByClassName("like-button");
      for (var i = 0; i < likeButtons.length; i++) {
        (function(button) {
          var gameId = button.getAttribute("data-game-id");
          // Fetch initial like count for this game
          fetchLikeCount(gameId);
          // Check if this browser already liked the game
          if (localStorage.getItem("liked-" + gameId)) {
            button.disabled = true;
            button.innerText = "Liked";
          }
          // Attach click event if not already liked
          button.addEventListener("click", function() {
            if (!localStorage.getItem("liked-" + gameId)) {
              sendLike(gameId);
            }
          });
        })(likeButtons[i]);
      }
    });
  </script>
